using System.IO;
using System.Linq;
using System.Text;
using Lighthouse.Core.Editor.ScriptableObject;
using UnityEditor;
using UnityEngine;

namespace Lighthouse.Core.Editor.ScriptGenerator
{
    [InitializeOnLoad]
    public static class SceneIdGenerator
    {
        static SceneIdGenerator()
        {
            EditorBuildSettings.sceneListChanged += OnSceneListChanged;
        }

        static void OnSceneListChanged()
        {
            GenerateMainSceneId();
            GenerateModuleSceneId();
        }

        [MenuItem("Lighthouse/Auto Generate/Generate Main Scene Id")]
        static void GenerateMainSceneId()
        {
            var generateSettings = LighthouseEditor.GetSettings<GenerateSettings>();
            GenerateSceneId("MainScene", generateSettings.MainSceneIdPrefix, generateSettings.MainSceneIdFilePath, generateSettings.ProductNameSpace);
        }

        [MenuItem("Lighthouse/Auto Generate/Generate Module Scene Id")]
        static void GenerateModuleSceneId()
        {
            var generateSettings = LighthouseEditor.GetSettings<GenerateSettings>();
            GenerateSceneId("ModuleScene", generateSettings.ModuleSceneIdPrefix, generateSettings.SceneModuleIdIdFilePath, generateSettings.ProductNameSpace);
        }

        static void GenerateSceneId(string sceneIdType, string prefix, string outputPath, string nameSpace)
        {
            var scenes = EditorBuildSettings.scenes
                .Where(s => s.enabled)
                .Select(s => s.path)
                .Where(p => !string.IsNullOrWhiteSpace(p) && p.Contains($"{sceneIdType}"))
                .Distinct()
                .ToArray();

            var content = BuildContent(sceneIdType, prefix, scenes, nameSpace);
            var result = WriteContent(content, outputPath);
            if (!result)
            {
                Debug.LogError("[SceneIdGenerator] Failed to generate SceneID");
                return;
            }

            AssetDatabase.ImportAsset(outputPath, ImportAssetOptions.ForceUpdate);
            AssetDatabase.Refresh();
            Debug.Log("[SceneIdGenerator] Generated.");
        }

        static string BuildContent(string sceneIdType, string prefix, string[] sceneNames, string nameSpace)
        {
            if (sceneNames.Length == 0)
            {
                Debug.LogError("[SceneIdGenerator] No enabled scenes found in Build Settings.");
            }

            var sb = new StringBuilder(8 * 1024);

            sb.AppendLine("// <auto-generated>");
            sb.AppendLine("// This file is generated by SceneIdGenerator.");
            sb.AppendLine("// DO NOT EDIT MANUALLY.");
            sb.AppendLine("// </auto-generated>");
            sb.AppendLine();
            sb.AppendLine("using System;");
            sb.AppendLine("using Lighthouse.Core;");
            sb.AppendLine("using Lighthouse.Core.Scene;");
            sb.AppendLine();
            sb.AppendLine($"namespace {nameSpace}");
            sb.AppendLine("{");
            sb.AppendLine($"    public static class {prefix}{sceneIdType}Id");
            sb.AppendLine("    {");

            byte id = 1;

            // None
            sb.AppendLine($"        public static readonly {sceneIdType}Id None = new {sceneIdType}Id({id}, string.Empty);");

            id++;

            foreach (var sceneName in sceneNames)
            {
                var validSceneName = Path.GetFileNameWithoutExtension(sceneName);
                var identifier = SanitizeIdentifier(validSceneName);

                sb.AppendLine($"        public static readonly {sceneIdType}Id {identifier} = new {sceneIdType}Id({id}, \"{validSceneName}\");");

                id++;
            }

            sb.AppendLine();
            sb.AppendLine($"        public static ReadOnlySpan<{sceneIdType}Id> All");
            sb.AppendLine("        {");
            sb.AppendLine("            get");
            sb.AppendLine("            {");
            sb.AppendLine($"                return new {sceneIdType}Id[]");
            sb.AppendLine("                {");
            sb.AppendLine("                    None,");

            foreach (var sceneName in sceneNames)
            {
                var validSceneName = Path.GetFileNameWithoutExtension(sceneName);
                var identifier = SanitizeIdentifier(validSceneName);

                sb.AppendLine($"                    {identifier},");
            }

            sb.AppendLine("                };");
            sb.AppendLine("            }");
            sb.AppendLine("        }");
            sb.AppendLine("    }");
            sb.AppendLine("}");

            return sb.ToString();
        }

        static bool WriteContent(string content, string sceneIdFilePath)
        {
            var directoryPath = Path.GetDirectoryName(sceneIdFilePath);
            if (!Directory.Exists(directoryPath))
            {
                Directory.CreateDirectory(directoryPath);
            }

            if (File.Exists(sceneIdFilePath))
            {
                var existing = File.ReadAllText(sceneIdFilePath, Encoding.UTF8);
                if (existing == content)
                {
                    return true;
                }
            }

            File.WriteAllText(sceneIdFilePath, content, new UTF8Encoding(encoderShouldEmitUTF8Identifier: false));
            return true;
        }

        static string SanitizeIdentifier(string name)
        {
            if (string.IsNullOrWhiteSpace(name))
            {
                return "_Scene";
            }

            var sb = new StringBuilder(name.Length);

            for (var i = 0; i < name.Length; i++)
            {
                var c = name[i];

                if (char.IsLetterOrDigit(c) || c == '_')
                {
                    sb.Append(c);
                }
                else
                {
                    sb.Append('_');
                }
            }

            if (char.IsDigit(sb[0]))
            {
                sb.Insert(0, '_');
            }

            return sb.ToString();
        }
    }
}